function nextYearSelector() {
  const ref = document.querySelector(".currDate");
  const errRef = document.querySelector(".error");
  if (errRef.classList.contains("show")) {
    errRef.classList.remove("show");
  }

  if (!(ref.innerHTML >= "2022")) {
    ref.innerHTML = parseInt(ref.innerHTML) + 1;
    currDateSelector(ref.innerHTML);
  } else {
    errRef.innerHTML = "Date limit Exceeded! Cannot Past 2022";
    errRef.classList.add("show");
  }
}
function prevYearSelector() {
  const ref = document.querySelector(".currDate");
  const errRef = document.querySelector(".error");
  if (errRef.classList.contains("show")) {
    errRef.innerHTML = "Date limit Exceeded! Cannot Go Beyond 1996";
    errRef.classList.remove("show");
  }

  if (!(ref.innerHTML <= "1996")) {
    ref.innerHTML = parseInt(ref.innerHTML) - 1;
    currDateSelector(ref.innerHTML);
  } else {
    errRef.innerHTML = "Date limit Exceeded! Cannot Go Beyond 1996";
    errRef.classList.add("show");
  }
}
